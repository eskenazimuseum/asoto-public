            <h2>About Advancing Women Artists</h2>
            <p>
                Between 2009 and 2021, Advancing Women Artists (AWA) identified over 2,000 ‘lost’ works by women artists in public collections in Italy and financed the conservation of seventy, including paintings and sculptures made between the sixteenth and twentieth-centuries. Notably, AWA funded the conservation of Artemisia Gentileschi’s <em>David and Bathsheba</em> (1645-50), a damaged work that had languished in the attic of the Pitti Palace for over 350 years. Although the organization has since closed, <a href="http://advancingwomenartists.org" target="_blank">its digital archive of restoration projects</a> remains online and its mission to advance understanding and appreciation of work by women artists continues, in part, through A Space of Their Own. 
            </p>
